def test_km_censoring_cr_no_censoring_yields_constant_one():
    time = np.array([1.0, 2.0, 3.0, 4.0])
    event = np.array([1, 1, 2, 2])
    tu, G = _km_censoring_cr(time, event)
    np.testing.assert_array_equal(tu, np.array([1.0, 2.0, 3.0, 4.0]))
    np.testing.assert_allclose(G, [1.0, 1.0, 1.0, 1.0])


def test_km_censoring_cr_pure_censoring_matches_textbook_km():
    # All censored — KM-of-censoring is the standard KM with one event per time.
    time = np.array([1.0, 2.0, 3.0, 4.0])
    event = np.array([0, 0, 0, 0])
    _, G = _km_censoring_cr(time, event)
    # Cumulative: 3/4, 3/4*2/3=1/2, 1/2*1/2=1/4, 1/4*0=0.
    expected = np.array([3 / 4, 1 / 2, 1 / 4, 0.0])
    np.testing.assert_allclose(G, expected)


def test_trap_mean_handles_all_nan():
    assert np.isnan(_trap_mean(np.array([1.0, 2.0]), np.array([np.nan, np.nan])))


def test_trap_mean_constant_returns_constant():
    times = np.array([1.0, 2.0, 3.0, 4.0])
    vals = np.full(4, 0.7)
    assert _trap_mean(times, vals) == pytest.approx(0.7)
