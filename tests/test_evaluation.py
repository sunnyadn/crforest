_CAL_COLS = [
    "model",
    "times",
    "predicted_decile",
    "observed_freq",
    "lower_ci",
    "upper_ci",
    "bin_n",
]


def _calib_predictions() -> tuple[pd.DataFrame, dict[str, np.ndarray], np.ndarray]:
    """Re-stack pbc CIF predictions onto the partner's calibration grid
    (1y, 3y, 5y, 10y in days). The committed pbc_predictions.csv carries
    six SUN-60 eval times (365, 730, 1460, 2190, 2920, 3650); for each
    target day we use the prediction at the nearest committed eval time
    that does not exceed the target — same convention as the matching
    R fixture script ``tests/cross_check_calibration.R``.
    """
    if not (_PBC_TEST.exists() and _PBC_PRED.exists() and _PBC_CALIB.exists()):
        pytest.skip("calibration fixtures missing — run `Rscript tests/cross_check_calibration.R`")
    test = pd.read_csv(_PBC_TEST)
    preds = pd.read_csv(_PBC_PRED)
    sun60_days = np.array([365, 730, 1460, 2190, 2920, 3650], dtype=float)
    target_days = np.array([365, 1095, 1825, 3650], dtype=float)
    src_idx = [int(np.searchsorted(sun60_days, t, side="right") - 1) for t in target_days]
    src_days = sun60_days[np.array(src_idx)].astype(int).tolist()
    full = np.column_stack([preds[f"RSF_full_t{d}"].to_numpy(float) for d in src_days])
    small = np.column_stack([preds[f"RSF_small_t{d}"].to_numpy(float) for d in src_days])
    return test, {"RSF_full": full, "RSF_small": small}, target_days


def test_calibration_cr_matches_riskregression_within_1e3():
    """Acceptance gate per SUN-64: per-bin observed_freq must match
    riskRegression::plotCalibration(method="quantile", q=10) within
    1e-3 absolute on every (model, time, bin)."""
    test, probs, target_days = _calib_predictions()
    ref = pd.read_csv(_PBC_CALIB)

    py = calibration_cr(
        probs,
        test["time"].to_numpy(float),
        test["event"].to_numpy(int),
        target_days,
    )
    py = py.sort_values(["model", "times", "predicted_decile"]).reset_index(drop=True)
    py["bin_idx"] = py.groupby(["model", "times"]).cumcount() + 1

    merged = ref.merge(
        py,
        left_on=["model", "time_days", "bin_idx"],
        right_on=["model", "times", "bin_idx"],
        how="inner",
    )
    assert len(merged) == len(ref) == 80, "row count mismatch"

    d_pred = (merged["Pred"] - merged["predicted_decile"]).abs()
    d_obs = (merged["Obs"] - merged["observed_freq"]).abs()
    d_n = (merged["bin_n_x"] - merged["bin_n_y"]).abs()
    assert d_pred.max() < 1e-12, f"max |dPred|={d_pred.max():.3e}"
    assert d_obs.max() < 1e-3, (
        f"max |dObs|={d_obs.max():.3e}; offending bins:\n"
        f"{merged.loc[d_obs >= 1e-3, ['model', 'time_days', 'bin_idx', 'Pred', 'Obs', 'observed_freq']]}"
    )
    assert d_n.max() == 0, f"bin_n mismatch: {d_n.max()}"


def test_calibration_cr_wilson_ci_hand_computed():
    """Hand-computed Wilson 95% CI on a 3-bin synthetic fixture."""
    rng = np.random.default_rng(7)
    n_per_bin = 30
    # Construct predictions in three tight clusters so quantile binning
    # produces three exactly-equal bins.
    p = np.concatenate(
        [
            rng.uniform(0.05, 0.06, n_per_bin),
            rng.uniform(0.30, 0.31, n_per_bin),
            rng.uniform(0.80, 0.81, n_per_bin),
        ]
    )
    # Plant cause-1 event rates per bin: 5/30, 12/30, 24/30. All events
    # at t=1.0 < eval; no censoring, no competing events.
    n_event = [5, 12, 24]
    event = np.concatenate(
        [np.array([1] * n_event[k] + [0] * (n_per_bin - n_event[k]), dtype=int) for k in range(3)]
    )
    time = np.where(event == 1, 1.0, 5.0)  # cause-1 at t=1, censored at t=5

    df = calibration_cr(
        {"M": p[:, None]},
        time,
        event,
        np.array([2.0]),
        cause=1,
        n_bins=3,
    )
    df = df.sort_values("predicted_decile").reset_index(drop=True)
    z = 1.959963984540054
    for k, k_event in enumerate(n_event):
        p_hat = k_event / n_per_bin
        lo_expected, hi_expected = _wilson_ci(p_hat, n_per_bin, z)
        assert df.loc[k, "observed_freq"] == pytest.approx(p_hat, abs=1e-12)
        assert df.loc[k, "lower_ci"] == pytest.approx(lo_expected, abs=1e-12)
        assert df.loc[k, "upper_ci"] == pytest.approx(hi_expected, abs=1e-12)
        assert df.loc[k, "bin_n"] == n_per_bin


def test_calibration_cr_returns_expected_columns():
    rng = np.random.default_rng(0)
    n, T = 200, 3
    time = rng.exponential(3.0, n)
    event = rng.choice([0, 1, 2], n, p=[0.4, 0.4, 0.2])
    probs = np.clip(rng.uniform(0, 0.5, (n, T)).cumsum(axis=1) / T, 0, 1)
    eval_times = np.array([1.0, 3.0, 5.0])
    df = calibration_cr({"A": probs, "B": probs[::-1]}, time, event, eval_times, n_bins=5)
    assert list(df.columns) == _CAL_COLS
    # Two models x T x bins, modulo dropped empty bins. Should be <= 30.
    assert 0 < len(df) <= 2 * T * 5


def test_calibration_cr_rejects_invalid_inputs():
    rng = np.random.default_rng(1)
    n, T = 50, 2
    time = rng.exponential(2.0, n)
    event = rng.choice([0, 1], n)
    probs = rng.uniform(0, 1, (n, T))
    eval_times = np.array([1.0, 2.0])

    with pytest.raises(ValueError, match="non-empty"):
        calibration_cr({"M": probs}, time, event, np.array([]))
    with pytest.raises(ValueError, match="strictly increasing"):
        calibration_cr({"M": probs}, time, event, np.array([2.0, 1.0]))
    with pytest.raises(ValueError, match="predictions"):
        calibration_cr({}, time, event, eval_times)
    with pytest.raises(ValueError, match="n_bins"):
        calibration_cr({"M": probs}, time, event, eval_times, n_bins=1)
    with pytest.raises(ValueError, match="confidence_level"):
        calibration_cr({"M": probs}, time, event, eval_times, confidence_level=1.5)
    with pytest.raises(ValueError, match="shape"):
        calibration_cr({"M": probs[:, :1]}, time, event, eval_times)


def test_score_cr_calibration_at_populates_result():
    """score_cr(..., calibration_at=...) populates result.calibration."""
    test, probs, target_days = _calib_predictions()
    eval_times = target_days  # use the same grid as calibration_at
    res = score_cr(
        probs,
        test["time"].to_numpy(float),
        test["event"].to_numpy(int),
        eval_times,
        calibration_at=target_days,
    )
    assert isinstance(res, ScoreResult)
    assert list(res.calibration.columns) == _CAL_COLS
    # 2 models * 4 times * 10 bins = 80 rows (no empty bins on pbc).
    assert len(res.calibration) == 80
    # Multi-time facet: every requested calibration time appears.
    assert sorted(res.calibration["times"].unique().tolist()) == sorted(
        float(d) for d in target_days
    )
    # Wilson CIs respect lower_ci <= observed_freq <= upper_ci.
    finite = res.calibration.dropna(subset=["observed_freq"])
    assert (finite["lower_ci"] <= finite["observed_freq"] + 1e-12).all()
    assert (finite["observed_freq"] <= finite["upper_ci"] + 1e-12).all()


