def _make_synth(n=300, T=5, seed=0):
    rng = np.random.default_rng(seed)
    eval_times = np.linspace(1.0, 5.0, T)
    time = rng.exponential(3.0, n)
    event = rng.choice([0, 1, 2], n, p=[0.4, 0.4, 0.2])
    probs = np.clip(rng.uniform(0, 0.3, (n, T)).cumsum(axis=1) / T, 0, 1)
    return time, event, probs, eval_times


def test_score_cr_returns_scoreresult_with_expected_columns():
    time, event, probs, eval_times = _make_synth()
    res = score_cr({"M": probs}, time, event, eval_times)
    assert isinstance(res, ScoreResult)
    assert list(res.auc.columns) == ["model", "times", "AUC", "lower", "upper"]
    assert list(res.brier.columns) == ["model", "times", "Brier", "lower", "upper"]
    assert list(res.iauc.columns) == ["model", "iAUC", "lower", "upper"]
    assert list(res.ibs.columns) == ["model", "IBS", "lower", "upper"]
    assert len(res.auc) == eval_times.size
    assert len(res.iauc) == 1


def test_score_cr_no_bootstrap_leaves_ci_nan():
    time, event, probs, eval_times = _make_synth()
    res = score_cr({"M": probs}, time, event, eval_times, n_bootstrap=0)
    assert res.auc["lower"].isna().all()
    assert res.auc["upper"].isna().all()
    assert res.brier["lower"].isna().all()
    assert res.brier["upper"].isna().all()


def test_score_cr_bootstrap_populates_ci():
    time, event, probs, eval_times = _make_synth()
    res = score_cr(
        {"M": probs},
        time,
        event,
        eval_times,
        n_bootstrap=20,
        random_state=42,
        n_jobs=1,
    )
    # CIs must be finite and lower <= point <= upper for finite point estimates.
    aw = res.auc.dropna(subset=["AUC"])
    assert aw["lower"].notna().all()
    assert aw["upper"].notna().all()
    assert (aw["lower"] <= aw["AUC"] + 1e-12).all()
    assert (aw["AUC"] <= aw["upper"] + 1e-12).all()


def test_score_cr_bootstrap_seed_is_reproducible():
    time, event, probs, eval_times = _make_synth()
    a = score_cr({"M": probs}, time, event, eval_times, n_bootstrap=15, random_state=7, n_jobs=1)
    b = score_cr({"M": probs}, time, event, eval_times, n_bootstrap=15, random_state=7, n_jobs=1)
    pd.testing.assert_frame_equal(a.auc, b.auc)
    pd.testing.assert_frame_equal(a.brier, b.brier)


def test_score_cr_multiple_models_independent():
    time, event, probs, eval_times = _make_synth()
    perfect = np.zeros_like(probs)
    cause_event = (event == 1).astype(float)
    # Perfect prognosticator at every eval time: assign 1 to actual cause-1 events, 0 else.
    perfect[:] = cause_event[:, None]
    res = score_cr({"random": probs, "perfect": perfect}, time, event, eval_times)
    a = res.auc.set_index(["model", "times"])["AUC"]
    auc_random = a.loc["random"].to_numpy()
    auc_perfect = a.loc["perfect"].to_numpy()
    finite = np.isfinite(auc_random) & np.isfinite(auc_perfect)
    assert finite.any()
    assert (auc_perfect[finite] >= auc_random[finite] - 1e-9).all()


def test_score_cr_metrics_subset_returns_only_requested():
    time, event, probs, eval_times = _make_synth()
    only_auc = score_cr({"M": probs}, time, event, eval_times, metrics=("auc",))
    assert len(only_auc.auc) == eval_times.size
    assert len(only_auc.iauc) == 1
    assert len(only_auc.brier) == 0
    assert len(only_auc.ibs) == 0

    only_brier = score_cr({"M": probs}, time, event, eval_times, metrics=("brier",))
    assert len(only_brier.brier) == eval_times.size
    assert len(only_brier.ibs) == 1
    assert len(only_brier.auc) == 0
    assert len(only_brier.iauc) == 0


def test_score_cr_handles_alternative_cause():
    """Asking for cause=2 should swap the case/control role of cause 1
    and cause 2 — predictions are then the CIF for cause 2."""
    time, event, probs, eval_times = _make_synth()
    res1 = score_cr({"M": probs}, time, event, eval_times, cause=1)
    res2 = score_cr({"M": probs}, time, event, eval_times, cause=2)
    # Both must produce finite AUCs in at least one cell.
    assert res1.auc["AUC"].notna().any()
    assert res2.auc["AUC"].notna().any()


