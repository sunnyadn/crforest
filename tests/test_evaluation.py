def test_score_cr_rejects_shape_mismatch():
    time, event, probs, eval_times = _make_synth()
    bad = probs[:, :-1]  # T-1 columns
    with pytest.raises(ValueError, match="shape"):
        score_cr({"M": bad}, time, event, eval_times)


def test_score_cr_rejects_unknown_metric():
    time, event, probs, eval_times = _make_synth()
    with pytest.raises(ValueError, match="unknown metrics"):
        score_cr({"M": probs}, time, event, eval_times, metrics=("auc", "ipa"))


def test_score_cr_rejects_empty_predictions():
    _, _, _, eval_times = _make_synth()
    with pytest.raises(ValueError, match="predictions"):
        score_cr({}, np.zeros(5), np.zeros(5, int), eval_times)


def test_score_cr_rejects_non_increasing_eval_times():
    time, event, probs, _ = _make_synth()
    with pytest.raises(ValueError, match="eval_times"):
        score_cr({"M": probs}, time, event, np.array([3.0, 1.0, 2.0, 4.0, 5.0]))


def test_score_cr_rejects_length_mismatch():
    _, _, probs, eval_times = _make_synth()
    with pytest.raises(ValueError, match="same length"):
        score_cr({"M": probs}, np.zeros(5), np.zeros(6, int), eval_times)


