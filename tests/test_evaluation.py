def test_scoreresult_is_picklable():
    time, event, probs, eval_times = _make_synth()
    res = score_cr({"M": probs}, time, event, eval_times, n_bootstrap=5, random_state=1, n_jobs=1)
    blob = pickle.dumps(res)
    res2 = pickle.loads(blob)
    pd.testing.assert_frame_equal(res.auc, res2.auc)
    pd.testing.assert_frame_equal(res.brier, res2.brier)


