"""Tests for comprisk.evaluation.score_cr + calibration_cr.

The cross-check fixtures are generated by tests/cross_check_riskRegression.R
(AUC + Brier) and tests/cross_check_calibration.R (calibration), committed
under tests/fixtures/, and pin the riskRegression::Score() and
riskRegression::plotCalibration numbers on a 70/30 split of the
survival::pbc dataset. Acceptance gates are per-(model, time) match within
2dp for AUC/Brier and per-(model, time, bin) match within 1e-3 absolute
for calibration.
"""

