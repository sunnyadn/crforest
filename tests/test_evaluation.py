def _load_pbc_fixture() -> tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame, np.ndarray]:
    if not (_PBC_TEST.exists() and _PBC_PRED.exists() and _PBC_SCORE.exists()):
        pytest.skip("pbc fixtures missing — run `Rscript tests/cross_check_riskRegression.R`")
    test = pd.read_csv(_PBC_TEST)
    preds = pd.read_csv(_PBC_PRED)
    ref = pd.read_csv(_PBC_SCORE)
    eval_times = np.array([365, 730, 1460, 2190, 2920, 3650], dtype=float)
    return test, preds, ref, eval_times


def _stack_cif(preds: pd.DataFrame, prefix: str, eval_times: np.ndarray) -> np.ndarray:
    cols = [f"{prefix}_t{int(t)}" for t in eval_times]
    return preds[cols].to_numpy(dtype=float)


@pytest.mark.parametrize("metric", ["AUC", "Brier"])
def test_score_cr_matches_riskregression_within_2dp(metric):
    test, preds, ref, eval_times = _load_pbc_fixture()
    probs = {
        "RSF_full": _stack_cif(preds, "RSF_full", eval_times),
        "RSF_small": _stack_cif(preds, "RSF_small", eval_times),
    }
    res = score_cr(
        probs,
        test["time"].to_numpy(float),
        test["event"].to_numpy(int),
        eval_times,
    )

    if metric == "AUC":
        py = res.auc[["model", "times", "AUC"]]
        col = "AUC"
    else:
        py = res.brier[["model", "times", "Brier"]]
        col = "Brier"

    merged = py.merge(ref[["model", "times", col]], on=["model", "times"], suffixes=("_py", "_r"))
    diff = (merged[f"{col}_py"] - merged[f"{col}_r"]).abs()
    # 2dp = within 0.005; we observe ~1e-16. Hard cap 0.005 with margin.
    assert (diff < 5e-3).all(), (
        f"{col} mismatch vs riskRegression (max |Δ|={diff.max():.6f}):\n{merged}"
    )


def test_score_cr_ibs_matches_riskregression():
    """IBS = trapezoidal time-average of Brier(t). riskRegression's IBS at
    the *largest* time corresponds to the cumulative-time average from
    ``eval_times[0]`` onward; we replicate that span."""
    test, preds, ref, eval_times = _load_pbc_fixture()
    probs = {
        "RSF_full": _stack_cif(preds, "RSF_full", eval_times),
        "RSF_small": _stack_cif(preds, "RSF_small", eval_times),
    }
    res = score_cr(
        probs,
        test["time"].to_numpy(float),
        test["event"].to_numpy(int),
        eval_times,
    )
    # Compare per-model trapezoidal IBS against the same trapezoidal
    # integration applied to the R-side per-time Brier numbers.
    for name in ["RSF_full", "RSF_small"]:
        ref_brier = ref[ref["model"] == name].sort_values("times")
        ours = res.ibs.loc[res.ibs["model"] == name, "IBS"].iloc[0]
        ref_ibs = _trap_mean(ref_brier["times"].to_numpy(), ref_brier["Brier"].to_numpy())
        assert abs(ours - ref_ibs) < 5e-3, (
            f"IBS for {name}: ours={ours:.5f} vs trap(ref Brier)={ref_ibs:.5f}"
        )


